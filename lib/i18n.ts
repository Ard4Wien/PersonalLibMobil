export type TranslationKey = keyof typeof translations.tr;

export const getLanguage = async (): Promise<Language> => {
    try {
        const lang = await AsyncStorage.getItem('user_language');
        const supportedLangs: Language[] = ['tr', 'en', 'fr', 'de', 'ja', 'zh', 'es', 'ru', 'pl', 'it', 'ar', 'pt'];
        return (lang && supportedLangs.includes(lang as Language)) ? lang as Language : 'tr';
    } catch {
        return 'tr';
    }
};

export const tStatic = (key: TranslationKey, lang: Language): string => {
    return translations[lang][key] || key;
};
